		local rightTurbineAngPos = clModel:GetAttachment( clModel:LookupAttachment( "RightTurbine" ) )
		local leftTurbineAngPos = clModel:GetAttachment( clModel:LookupAttachment( "LeftTurbine" ) )
		if not ( rightTurbineAngPos ) then rightTurbineAngPos = { Pos = clModel:GetPos( ), Ang = Angle( 0, 0, 0 ) } end
		if not ( leftTurbineAngPos ) then leftTurbineAngPos = { Pos = clModel:GetPos( ), Ang = Angle( 0, 0, 0 ) } end
		local particleEmitter = ParticleEmitter( clModel:GetPos( ) )
		local luaParticle = particleEmitter:Add( "particles/flamelet2", rightTurbineAngPos.Pos )
		local luaParticleTwo = particleEmitter:Add( "particles/flamelet2", leftTurbineAngPos.Pos )
		if ( luaParticle ) then
			luaParticle:SetColor( 255, 255, 255, math.random( 100, 200 ) )
			luaParticle:SetVelocity( Vector( 0, 0, math.random( -30, -70 ) ) )
			luaParticle:SetDieTime( 0.5 )
			luaParticle:SetLifeTime( 0 )
			luaParticle:SetStartSize( 5 )
			luaParticle:SetEndSize( 1 )
		end
		if ( luaParticleTwo ) then
